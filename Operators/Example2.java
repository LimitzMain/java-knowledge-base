public class ArithDeclaration {
    public static void main(String[] args) {
       
        //Arithmetic operators usage, with declaration.
        
        int x = 10;
        int y = 10;
        
        //Addition
        x += 5; // 15
        
        //Subtraction
        y -= 10; // 0
        
        System.out.println("X value after addition: " + x);
        System.out.println("Y value after subtraction: " + y);
        
        //Multiplcation
        x *= 20; // 300
        
        System.out.println("X after multiplication: " + x);
        
        //Division
        x /= 10; // 300 / 10 = 30
        
        System.out.println("X after division: " + x);
    }
}
